const readFile = async (filename: string): Promise<string> => {
  return new Promise((resolve, reject) => {
    fs.readFile(filename, (err, data) => {
      if (err) {
        reject(err);
      } else {
        const bufferString = data.toString("utf8");
        resolve(bufferString);
      }
    });
  });
};

